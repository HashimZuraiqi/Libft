#include <stdio.h>
int	ft_isalnum(char x)
{
    if ((x >= 65 && x <= 90) || (x >= 97 && x <= 122) || (x >= 48 && x <= 57))
    {
        return (1);
    }
    else
    {
        return (0);
    }
}