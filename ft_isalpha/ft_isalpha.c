#include <stdio.h>

int	ft_isalpha(char x)
{
	if ((x >= 65 && x <= 90) || (x >= 97 && x <= 122))
	{
		return (0);
	}
	else
	{
		return (-1);
	}
}

int	main(int argc, char *argv[])
{
	if (argc == 2)
	{
		printf("%d", ft_isalpha(*argv[1]));
	}
}
